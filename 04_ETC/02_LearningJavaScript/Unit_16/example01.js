const x = 1900.51;

console.log(x.toFixed(3)) //1900.510
console.log(x.toFixed(2)) //1900.51
console.log(x.toFixed(1)) //1900.5
console.log(x.toFixed(0)) //1901

console.log(x.toExponential(3)) //1.901e+3
console.log(x.toExponential(2)) //1.90e+3
console.log(x.toExponential(1)) //1.9e+3
console.log(x.toExponential(0)) //2e+3

