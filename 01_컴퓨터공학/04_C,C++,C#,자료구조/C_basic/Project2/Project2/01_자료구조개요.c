/*
- 자료구조의 필요성
	- 데이터를 효과적으로 저장하고, 처리하는 방법에 대해 바르게 이해
	- 자료구조를 제대로 이해하면, 불필요한 메모리 및 성능 낭비를 줄일 수 있다.
- 기본 자료구조 (정리)
	- 선형구조
		- 배열
		- 연결리스트
		- 스택
		- 큐
	- 비 선형구조
		- 트리
		- 그래프

- 효율적인 자료구조 설계를 위해 알고리즘 지식 필요
- 효율적인 알고리즘 작성하기 위해서는 문제 상황에 맞는 적절한 자료구조 사용되어야 함.
- 따라서, 자료구조론과 알고리즘 이른 모두 동일선상

- 성능측정
	- 시간복잡도: 알고리즘에 사용되는 연산 횟수
		- Big-O 표기법 (O(N), O(NlogN)...)
		- 최악의 경우를 나타냄
	- 공간복잡도: 알고리즘에 사용되는 메모리의 양 (예시)
		- int a[1000] = 4KB
		- int a[1000000] = 4KB
		- int a[2000][2000] = 16KB

*/